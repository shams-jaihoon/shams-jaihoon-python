# access list
myFriends = ["Ahmad", "Zafar", "Mansour", "Ghawsuddin", "Ghulam Rasoul"]

print(myFriends[1:4])


# change list
myFriends = ["Ahmad", "Zafar", "Mansour", "Ghawsuddin", "Ghulam Rasoul"]
